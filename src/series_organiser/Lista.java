public void SelectSer()
{
    try{
        Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Series1","andrei","andrei");
        St = Con.createStatement();
        Rs = St.executeQuery("select * from SERIESTBL");
        SeriesTable.setModel(DbUtils.resultSetToTableModel(Rs));
    }catch(SQLException e)
    {
        e.printStackTrace();
    }
}
private void GetGen()
{
    try{
        Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Series1","andrei","andrei");
        St = Con.createStatement();
        String query = "select * from andrei.CATEGORYTBL";
        Rs = St.executeQuery(query);
        while(Rs.next())
        {
            String MyGen = Rs.getString("GENNAME");
            GenCb.addItem(MyGen);
        }
    }catch(Exception e){
        
    }
}
    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked

        try{
            Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Series1","andrei","andrei");
            PreparedStatement add = Con.prepareStatement("insert into SERIESTBL values(?,?,?,?,?)");
            add.setInt(1, Integer.valueOf(SerID.getText()));
            add.setString(2,SerName.getText());
            add.setInt(3, Integer.valueOf(SerNo.getText()));
            add.setString(4,SerDesc.getText());
            add.setString(5, GenCb.getSelectedItem().toString());
            int row = add.executeUpdate();
            JOptionPane.showMessageDialog(this,"Series Successfully Added");
            Con.close();
            SelectSer();
        }catch(SQLException e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_AddBtnMouseClicked

    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        if(SerID.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Enter the Series to be Deleted");
        }
        else
        {
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Series1","andrei","andrei");
                String Id = SerID.getText();
                String Query = "Delete from andrei.SERIESTBL where SERID="+Id;
                Statement Add = Con.createStatement();
                Add.executeUpdate(Query);
                SelectSer();
                JOptionPane.showMessageDialog(this, "Series Deleted Successfully");
            }catch(SQLException e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

    private void SeriesTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SeriesTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) SeriesTable.getModel();
        int Myindex = SeriesTable.getSelectedRow();
        SerID.setText(model.getValueAt(Myindex,0).toString());
        SerName.setText(model.getValueAt(Myindex,1).toString());
        SerNo.setText(model.getValueAt(Myindex,2).toString());
        SerDesc.setText(model.getValueAt(Myindex,3).toString());
    }//GEN-LAST:event_SeriesTableMouseClicked

