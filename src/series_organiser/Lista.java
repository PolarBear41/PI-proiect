    private void UpdateBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UpdateBtnMouseClicked
        if(SerID.getText().isEmpty()||SerName.getText().isEmpty()||SerNo.getText().isEmpty()||SerDesc.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else
        {
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Series1","andrei","andrei");
                String UpdateQuery = "update andrei.SERIESTBL set SERNAME='"+SerName.getText()+"'"+", SERNO="+SerNo.getText()+""+",SERDESC='"+SerDesc.getText()+"'"+",SERGEN='"+GenCb.getSelectedItem().toString()+"'"+"where SERID ="+SerID.getText();
                Statement Add = Con.createStatement();
                Add.executeUpdate(UpdateQuery);
                JOptionPane.showMessageDialog(this, "Series Updated Successfully");
                SelectSer();
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_UpdateBtnMouseClicked

