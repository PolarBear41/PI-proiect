    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
        // TODO add your handling code here:
        if(GenId.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Enter the Genre to be Deleted");
        }
        else
        {
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Series1","andrei","andrei");
                String Id = GenId.getText();
                String Query = "Delete from andrei.CATEGORYTBL where GENID="+Id;
                Statement Add = Con.createStatement();
                Add.executeUpdate(Query);
                SelectGen();
                JOptionPane.showMessageDialog(this, "Genre Deleted Successfully");
            }catch(SQLException e)
            {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_DeleteBtnMouseClicked

    private void EditBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditBtnMouseClicked
        if(GenId.getText().isEmpty()||GenName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Missing Information");
        }
        else
        {
            try{
                Con = DriverManager.getConnection("jdbc:derby://localhost:1527/Series1","andrei","andrei");
                String UpdateQuery = "update andrei.CATEGORYTBL set GENNAME='"+GenName.getText()+"'"+"where GENID ="+GenId.getText();
                Statement Add = Con.createStatement();
                Add.executeUpdate(UpdateQuery);
                JOptionPane.showMessageDialog(this, "Genre Updated Successfully");
                SelectGen();
            }catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_EditBtnMouseClicked

